// "한마디" 카드용 정적 문구 풀. 날짜 기반 인덱스로 하루 하나 고정(랜덤 미사용).
const PHRASES = [
  "오늘 하루, 너무 애쓰지 않아도 괜찮아요.",
  "작은 한 걸음도 한 걸음이에요.",
  "지금 이 순간의 나로 충분해요.",
  "잘 쉬는 것도 하루의 할 일이에요.",
  "어제보다 조금 나아졌다면 그걸로 충분해요.",
  "마음이 무거우면 잠깐 내려놓아도 돼요.",
  "오늘의 나에게 다정하게.",
  "비가 와도 우산이 있으니까요.",
  "할 수 있는 만큼만, 그게 최선이에요.",
  "당신은 생각보다 잘 해내고 있어요.",
  "숨 한 번 깊게 쉬고 시작해요.",
  "완벽하지 않아도 괜찮아요.",
  "오늘 한 가지만 잘해도 성공이에요.",
  "스스로를 너무 다그치지 말기.",
  "느려도 멈추지 않으면 도착해요.",
];

/** YYYY-MM-DD 날짜 문자열을 받아 그날의 문구를 결정적으로 고른다. */
export function phraseForDate(dateStr: string): string {
  let h = 0;
  for (let i = 0; i < dateStr.length; i++) {
    h = (h * 31 + dateStr.charCodeAt(i)) >>> 0;
  }
  return PHRASES[h % PHRASES.length];
}
